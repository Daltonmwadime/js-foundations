//1.logical AND (&&)
//AND operator returns true if both operands are true, otherwise it returns false.
//AND looks for the first false value and if it finds it, it returns false without evaluating the second operand. If it does not find any false value, it returns true.
//In the logical reads from left to right, it evaluates the first operand and if it is false, it returns false without evaluating the second operand. If the first operand is true, it evaluates the second operand and returns its value.
//In the case of true && false, the result is false because one of the operands is false.
//In the case of false && false, the result is also false because both operands are false.
//In the case of true && true, the result is true because both operands are true.

console.log(true && true);
console.log(true && false);
console.log(false && false);
console.log(7&&3*2);//In JavaScript, the logical AND operator (&&) can also be used for short-circuit evaluation. In the expression 7 && 3 * 2, the logical AND operator evaluates the first operand 7. Since 7 is a non-zero number, it is considered truthy in JavaScript. Therefore, the logical AND operator evaluates the second operand 3 * 2, which equals 6. As a result, the logical AND operator returns 6 as the final result. This is an example of short-circuit evaluation, where the second operand is only evaluated if the first operand is truthy.
console.log(0&&3*2);//In the expression 0 && 3 * 2, the logical AND operator evaluates the first operand 0. Since 0 is a falsy value in JavaScript, the logical AND operator does not evaluate the second operand 3 * 2 and immediately returns 0 as the final result. This is another example of short-circuit evaluation, where the second operand is not evaluated because the first operand already determines the result.
